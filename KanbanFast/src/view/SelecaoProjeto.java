public void atualizaListaProjetos(){
        
       this.jComboBox1.removeAllItems();
       for (int i = 0; i < program.minhaEmpresa.getListaProjetos().size(); i++) {            
            this.jComboBox1.addItem(program.minhaEmpresa.getListaProjetos().get(i).getNome());
        }                                
               
        if (this.jComboBox1.getItemCount() > 0){
            this.jComboBox1.setSelectedIndex(program.getProjetoAtivo());      
        }
    }    
    
