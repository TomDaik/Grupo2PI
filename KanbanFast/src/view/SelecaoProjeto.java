    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                // Exibe a caixa de diálogo de entrada de texto

        String userInput = JOptionPane.showInputDialog(null, "Digite o nome da projeto que deseja adicionar:");
        
        // Verifica se o usuário clicou em OK ou Cancelar
        if (userInput != null) {
            // Exibe a caixa de diálogo com os botões personalizados
            Object[] options = {"OK", "Cancelar"};
            int selection = JOptionPane.showOptionDialog(
                    null,
                    "Você digitou: " + userInput +   ". Deseja continuar?",
                    "Confirmação",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    options,
                    options[0]); // Índice do botão padrão (neste caso, "OK")

            // Verifica a opção selecionada pelo usuário
            if (selection == JOptionPane.OK_OPTION) {                                                
                Date dataInicio = new Date();
                Date dataFim = new Date();                
                ProjetoEmpresa meuProjeto = new ProjetoEmpresa(userInput, dataInicio, dataFim, userInput, StatusSituacao.NO_PRAZO, program.minhaEmpresa, 0.00);                        
                program.minhaEmpresa.addListaProjetos(meuProjeto);
                this.jComboBox1.addItem(userInput);
                this.jComboBox1.setSelectedIndex(this.jComboBox1.getItemCount()-1);                                                
                System.out.println(program.getProjetoAtivo());
                program.setProjetoAtivo(program.getProjetoAtivo());
                JOptionPane.showMessageDialog(null, "Operação executada com sucesso.");
            } else if (selection == JOptionPane.CANCEL_OPTION || selection == JOptionPane.CLOSED_OPTION) {
                JOptionPane.showMessageDialog(null, "Operação cancelada.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operação cancelada.");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

