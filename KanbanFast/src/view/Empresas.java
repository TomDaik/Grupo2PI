    public void atualizaDepartamentos(){
        
        // Cria um modelo de tabela vazio
        DefaultTableModel model = new DefaultTableModel();

        // Define as colunas no modelo (caso necess치rio)
        model.addColumn("Departamento");
        model.addColumn("Respons치vel");
                
        for (Departamento departamento : program.minhaEmpresa.getListaDepartamentos()) {
            String nomeFuncionario = (departamento.getFuncionarioResponsavel() != null) ? departamento.getFuncionarioResponsavel().getNomeFuncionario() : "N/A";        
            model.addRow(new Object[]{departamento.getNomeDepartamento(), nomeFuncionario});        
            
        }        
        // Define o modelo vazio na tabela
        this.jTable1.setModel(model);        
    }

