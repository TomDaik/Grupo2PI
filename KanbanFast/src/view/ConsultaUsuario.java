    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
  int linhaSelecionada = jTable1.getSelectedRow();

        if (linhaSelecionada != -1) {

            if (linhaSelecionada == 0) {
                JOptionPane.showMessageDialog(null, "Você não pode remover este usuário.");
            } else {
                Object[] options = {"OK", "Cancelar"};
                int selection = JOptionPane.showOptionDialog(null,
                        "Você deseja continuar e remover o usuário?",
                        "Confirmação",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE,
                        null,
                        options,
                        options[0]); // Índice do botão padrão (neste caso, "OK")

                // Verifica a opção selecionada pelo usuário
                if (selection == JOptionPane.OK_OPTION) {
                    //program.minhaEmpresa.getListaDepartamentos().get(0).removeListaFuncionarios();
                    atualizaUsuarios();
                } else if (selection == JOptionPane.CANCEL_OPTION || selection == JOptionPane.CLOSED_OPTION) {
                    JOptionPane.showMessageDialog(null, "Operação cancelada.");

                }
            }

        } else {
            // Nenhuma linha está selecionada
            System.out.println("Nenhuma linha selecionada");
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

