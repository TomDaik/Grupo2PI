    private void atualizaUsuarios(){
        
        // Cria um modelo de tabela vazio
        DefaultTableModel model = new DefaultTableModel();

        // Define as colunas no modelo (caso necessário)
        model.addColumn("Nome Usuário");
        model.addColumn("CPF");       
        model.addColumn("Departamento");       
        model.addColumn("Matrícula");           
                        
        for (int i = 0; i < program.meuDepartamento.getListaFuncionarios().size(); i++) {            
            model.addRow(new Object[]{program.meuDepartamento.getListaFuncionarios().get(i).getNomeFuncionario(), 
                program.meuDepartamento.getListaFuncionarios().get(i).getCpf(),
                program.meuDepartamento.getListaFuncionarios().get(i).getDepartamento().getNomeDepartamento(),
                program.meuDepartamento.getListaFuncionarios().get(i).getMatricula()});                 
        }                                
        
        // Define o modelo vazio na tabela
        this.jTable1.setModel(model);                        
        
        
        for (Departamento departamento : program.minhaEmpresa.getListaDepartamentos()) {
            this.jComboBox1.addItem(departamento.getNomeDepartamento());
        }        
        
    }

